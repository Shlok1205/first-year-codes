a = {} # Empty dictionary
print(type(a))  # <class 'dict'>

a = [] # Empty list
print(type(a))  # <class 'list'>

a = {1, 2} # Set
print(type(a))  # <class 'set'>

a = set() # Empty set
print(type(a))  # <class 'set'>

a = ()  # Empty tuple   
print(type(a))  # <class 'tuple'>

a = {1,2,3,1,2}
print(len(a))
print(a)

a = {"Verma","Sharma","Gupta"}
print(a)

a = {1,"Verma",2,"Sharma"}
print(a)

